    public function get_file_extension($file_name) {
        try {
            $file = new File($file_name);
            $ext = $file->ext();
            return $ext;
        } catch (Exception $exception) {
            echo $exception->getMessage();
        }
    }

    public function get_file_name_without_extension($file_name) {
        try {
            $ext = $this->get_file_extension($file_name);
            $file_name = str_replace("." . $ext, "", $file_name);
            return $file_name;
        } catch (Exception $exception) {
            echo $exception->getMessage();
        }
    }

    function create_dir($path) {
        try {
            $document_root_path = $_SERVER['DOCUMENT_ROOT'];
            //$document_root_path =  $_SERVER['DOCUMENT_ROOT'].DS.'mybuy4u';
            $path = $document_root_path.$path;
            if (!is_dir($path)) {
                mkdir($path, 0705, true);
            }
        } catch (Exception $exception) {
            echo $exception->getMessage();
        }
    }

	function get_image_resize_height ($file, $width) {
		try {
			$file_name = $file["name"];
			if (!empty ($file_name)) {
				list($image_width, $image_height, $image_type, $image_attr) = getimagesize($file["tmp_name"]);
				return intval(($image_height*$width) / $image_width);
			}
		}catch (Exception $exception) {
            echo $exception->getMessage();
        }
	}

	function get_proper_file_name ($file_name) {
		try {
			$file_name = trim($file_name);
			$search = array(" ", "+", "(", ")");
			$file_name = str_replace($search, '_', $file_name);
			return $file_name;
		}catch (Exception $exception) {
            echo $exception->getMessage();
        }
	}

    function get_file_ext ($file_name) {
        try {
            $file_base_name = substr($file_name, 0, strripos($file_name, '.')); // strip extension
            $file_ext      = substr($file_name, strripos($file_name, '.'));
            $ext = end(explode(".", $file_name));
            return $ext;
        } catch (Exception $exception) {
            echo $exception->getMessage();
        }
    }
